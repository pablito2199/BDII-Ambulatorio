    //Permite prestar el ejemplar
    private void btnNuevoPrestamoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNuevoPrestamoActionPerformed
        //Creamos el modelo de tabla préstamos
        ModeloTablaPrestamos m;
        m = (ModeloTablaPrestamos) tablaPrestamos.getModel();
        //Si el usuario seleccionado tiene préstamos vencidos
        if ((Integer) m.getValueAt(tablaPrestamos.getSelectedRow(), 3) > 0) {
            //No se le permite llevar el ejemplar prestado.
            //Salta la excepción y se muestra en la ventana
            fa.muestraExcepcion("No se puede prestar el ejemplar. El usuario seleccionado tiene préstamos vencidos.");
            //Regresamos
            return;
        }
        //En otro caso le prestamos el ejemplar
        ejemplar.setUsuario((String) m.getValueAt(tablaPrestamos.getSelectedRow(), 0));
        fa.nuevoPrestamo(libro, ejemplar);
        //Actualizamos la tabla de ejemplares
        padre.actualizarTablaEjemplares();
        //Una vez prestado destruimos la ventana
        this.dispose();
    }//GEN-LAST:event_btnNuevoPrestamoActionPerformed

