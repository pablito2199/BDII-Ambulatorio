    public void buscarPrestamos() {
        //Creamos el modelo de tabla préstamos
        ModeloTablaPrestamos m;
        m = (ModeloTablaPrestamos) tablaPrestamos.getModel();
        //Se buscan los usuarios que cumplen el criterio de búsqueda y se menten en la tabla
        m.setFilas(fa.consultarPUsuarios((buscaId.getText().isEmpty()) ? null : buscaId.getText(), buscaNombre.getText()));
        //Si hay alguno se selecciona el primero
        if (m.getRowCount() > 0) {
            tablaPrestamos.setRowSelectionInterval(0, 0);
        }
        //De no haberlo se regresa sin hacer nada.
    }

